const mongoose = require("mongoose");
const { toJSON } = require("./plugins");

const blogSchema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true,
    },
    content: {
      type: String,
      required: true,
      trim: true,
    },
    author: {
      type: String,
      required: true,
      trim: true,
    },
    userId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

blogSchema.plugin(toJSON);

const Blog = mongoose.model("Blog", blogSchema);

module.exports = Blog;
